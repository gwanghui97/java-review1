package javabasic;

public class Example02 {
    public static void main(String[] args) {
        /**
         * 두 개의 int 변수를 선언하고,
         * 합, 빼기, 곱, 나눗셈 결과를 출력하는 프로그램
         */
        int leftNumber = 10;
        int rightNumber = 5;

        System.out.println(leftNumber + rightNumber);
        System.out.println(leftNumber - rightNumber);
        System.out.println(leftNumber * rightNumber);
        System.out.println(leftNumber / rightNumber);
    }
}
